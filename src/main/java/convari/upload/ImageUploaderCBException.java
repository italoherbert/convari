package convari.upload;


public class ImageUploaderCBException extends Exception {

	private static final long serialVersionUID = 1L;

	public ImageUploaderCBException() {
		super();
	}

	public ImageUploaderCBException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public ImageUploaderCBException(String msg) {
		super(msg);
	}

	public ImageUploaderCBException(Throwable cause) {
		super(cause);
	}

}