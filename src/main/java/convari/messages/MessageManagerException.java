package convari.messages;

public class MessageManagerException extends Exception {

	private static final long serialVersionUID = 1L;

	public MessageManagerException() {
		super();
	}

	public MessageManagerException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public MessageManagerException(String msg) {
		super(msg);
	}

	public MessageManagerException(Throwable cause) {
		super(cause);
	}

}
