package convari.response;

public class ResponseBuilderException extends Exception {

	private static final long serialVersionUID = 1L;

	public ResponseBuilderException() {
		super();
	}

	public ResponseBuilderException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public ResponseBuilderException(String msg) {
		super(msg);
	}

	public ResponseBuilderException(Throwable cause) {
		super(cause);
	}

}
