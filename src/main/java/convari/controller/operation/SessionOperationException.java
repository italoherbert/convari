package convari.controller.operation;

public class SessionOperationException extends OperationException {

	private static final long serialVersionUID = 1L;

	public SessionOperationException() {
		super();
	}

	public SessionOperationException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public SessionOperationException(String msg) {
		super(msg);
	}

	public SessionOperationException(Throwable cause) {
		super(cause);
	}

}
